function tryRepairMojibake(text: string) {
    if (!text || typeof TextDecoder === 'undefined') return text
    if (!/[Ãâðïœžš¢€™“”–—]/.test(text)) return text

    try {
        const bytes = Uint8Array.from(Array.from(text, (char) => char.charCodeAt(0) & 0xff))
        const repaired = new TextDecoder('utf-8', { fatal: false }).decode(bytes)
        return repaired || text
    } catch {
        return text
    }
}

function buildNormalizedLookupMap(source: Record<string, string>) {
    const normalized: Record<string, string> = {}

    for (const [key, value] of Object.entries(source)) {
        if (!(key in normalized)) normalized[key] = value

        const repairedKey = tryRepairMojibake(key)
        if (repairedKey && !(repairedKey in normalized)) {
            normalized[repairedKey] = value
        }
    }

    return normalized
}

const normalizedTextTranslations: Record<AppLocale, Record<string, string>> = {
    de: buildNormalizedLookupMap(textTranslations.de),
    en: buildNormalizedLookupMap(textTranslations.en),
}

const normalizedKeyTranslations: Record<AppLocale, Record<string, string>> = {
    de: buildNormalizedLookupMap(keyTranslations.de),
    en: buildNormalizedLookupMap(keyTranslations.en),
}

function splitDecorativePrefix(text: string) {
    const match = text.match(/^([\s\p{Extended_Pictographic}\p{Emoji_Presentation}\p{Symbol}\uFE0F\u200D\u2190-\u21FF]+)(.+)$/u)
    if (!match) return null

    const [, prefix, rest] = match
    if (!rest?.trim()) return null
    return { prefix, rest: rest.trimStart() }
}

